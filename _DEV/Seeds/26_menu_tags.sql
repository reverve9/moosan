-- ============================================================================
-- 26_menu_tags.sql
-- food_menus 에 자유 태그 컬럼 추가
-- ============================================================================
--
-- 부스 단위는 "구역"(food_categories) 으로 정리됐고, 메뉴는 자유 태그(다중)로
-- 표시. 카테고리 마스터를 별도로 두지 않고 string[] 자유 입력.
--
-- 어드민에서 매장 상세 모달의 메뉴 입력 행에 칩 입력 UI 가 붙고, 기존 사용
-- 태그를 자동완성으로 제공해서 표기 통일을 유도. 손님 화면은 부스 모달의
-- 메뉴 라인 옆에 작은 뱃지로 노출.
-- ============================================================================

ALTER TABLE food_menus
  ADD COLUMN IF NOT EXISTS tags TEXT[] NOT NULL DEFAULT '{}';

COMMENT ON COLUMN food_menus.tags IS
  '메뉴에 붙는 자유 태그 배열. 어드민에서 칩 입력으로 관리, 손님 화면에 뱃지로 노출.';

